// LibreTranslate API integration
const LIBRE_TRANSLATE_API_URL = "https://libretranslate.de/translate";

interface TranslationRequest {
  q: string;
  source: string;
  target: string;
  format?: string;
}

interface TranslationResponse {
  translatedText: string;
  detectedLanguage?: {
    confidence: number;
    language: string;
  };
}

// Language code mapping for LibreTranslate
const LANGUAGE_MAPPING: Record<string, string> = {
  en: "en",
  hi: "hi",
  kn: "kn", 
  te: "te",
  ta: "ta",
};

// LibreTranslate API function
export async function translateTextWithLibreTranslate(
  text: string,
  targetLanguage: string,
  sourceLanguage: string = "en"
): Promise<string> {
  try {
    // Don't translate if target language is English or text is empty
    if (targetLanguage === "en" || !text.trim()) {
      return text;
    }

    const targetLang = LANGUAGE_MAPPING[targetLanguage];
    if (!targetLang) {
      console.warn(`Unsupported language: ${targetLanguage}`);
      return text;
    }

    const requestBody: TranslationRequest = {
      q: text,
      source: sourceLanguage,
      target: targetLang,
      format: "text",
    };

    const response = await fetch(LIBRE_TRANSLATE_API_URL, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify(requestBody),
    });

    if (!response.ok) {
      throw new Error(`Translation failed: ${response.status} ${response.statusText}`);
    }

    const data: TranslationResponse = await response.json();
    return data.translatedText;
  } catch (error) {
    console.error("LibreTranslate error:", error);
    // Return original text if translation fails
    return text;
  }
}

// Translate all monument fields using LibreTranslate
export async function translateMonumentFieldsWithAPI(
  monument: any,
  targetLanguage: string
): Promise<any> {
  if (targetLanguage === "en") {
    return monument;
  }

  const fieldsToTranslate = [
    "description",
    "significance", 
    "history",
    "architecture",
    "bestTimeToVisit",
    "photography",
    "howToReach"
  ];

  const translatedMonument = { ...monument };

  // Translate text fields
  for (const field of fieldsToTranslate) {
    if (monument[field] && typeof monument[field] === "string") {
      translatedMonument[field] = await translateTextWithLibreTranslate(
        monument[field],
        targetLanguage
      );
    }
  }

  // Translate array fields
  if (monument.historicalEvents && Array.isArray(monument.historicalEvents)) {
    translatedMonument.historicalEvents = await Promise.all(
      monument.historicalEvents.map((event: string) =>
        translateTextWithLibreTranslate(event, targetLanguage)
      )
    );
  }

  if (monument.architecturalFeatures && Array.isArray(monument.architecturalFeatures)) {
    translatedMonument.architecturalFeatures = await Promise.all(
      monument.architecturalFeatures.map((feature: string) =>
        translateTextWithLibreTranslate(feature, targetLanguage)
      )
    );
  }

  if (monument.nearbyAttractions && Array.isArray(monument.nearbyAttractions)) {
    translatedMonument.nearbyAttractions = await Promise.all(
      monument.nearbyAttractions.map((attraction: string) =>
        translateTextWithLibreTranslate(attraction, targetLanguage)
      )
    );
  }

  return translatedMonument;
}

